<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class ContactList extends Model
{
    public $table = "contact_lists";
	protected $dates = ['deleted_at'];

	public $fillable = [
		'name',
		'description',
	];

	protected $casts = [
		'name'=>'string',
		'description'=>'string',
	];

	//-----------------------------------------
	// Traits Start
	//-----------------------------------------
	
	public function getResistableRelations() {
		return [];
	}

	//-----------------------------------------
	// Traits End
	//-----------------------------------------

	//-----------------------------------------
	// Relations Start
	//-----------------------------------------
	public function contacts()
	{
		return $this->hasMany(\App\Contact::class);
	}
	//-----------------------------------------
	// Relations End
	//-----------------------------------------

	//-----------------------------------------
	// Attributes Start
	//-----------------------------------------
	//-----------------------------------------
	// Attributes End
	//-----------------------------------------

	//-----------------------------------------
	// Scopes Start
	//-----------------------------------------
	//-----------------------------------------
	// Scopes End
	//-----------------------------------------

	//-----------------------------------------
	// Functions Start
	//-----------------------------------------
	//-----------------------------------------
	// Functions End
	//-----------------------------------------
}
